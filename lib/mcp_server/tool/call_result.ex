defmodule McpServer.Tool.CallResult do
  @moduledoc """
  Extended tool call result supporting structured content for UI rendering.

  Controllers can return this struct instead of a plain content list to include
  `structured_content` (structured data optimized for UI rendering) alongside
  the standard `content` (text representation for model context).

  ## Fields

  - `content` (required) - List of content blocks (`McpServer.Tool.Content.Text`,
    `McpServer.Tool.Content.Image`, `McpServer.Tool.Content.Resource`).
    This is the text representation included in model context.
  - `structured_content` - Map of structured data optimized for UI rendering.
    Excluded from model context. Used by views to render rich interfaces.
  - `_meta` - Additional metadata (timestamps, source info). Not included
    in model context.

  ## Usage

  Return from a tool controller function:

      def get_weather(_conn, %{"location" => location}) do
        weather = fetch_weather(location)

        {:ok, McpServer.Tool.CallResult.new(
          content: [McpServer.Tool.Content.text("Weather in \#{location}: \#{weather.temp}°F")],
          structured_content: %{
            "temperature" => weather.temp,
            "unit" => "fahrenheit",
            "humidity" => weather.humidity,
            "forecast" => weather.forecast
          }
        )}
      end

  For backward compatibility, controllers can still return a plain content list:

      def echo(_conn, %{"message" => message}) do
        {:ok, [McpServer.Tool.Content.text(message)]}
      end
  """

  @enforce_keys [:content]
  defstruct [
    :content,
    :structured_content,
    :_meta
  ]

  @type t :: %__MODULE__{
          content: list(),
          structured_content: map() | nil,
          _meta: map() | nil
        }

  @doc """
  Creates a new CallResult struct.

  ## Parameters

  - `opts` - Keyword list of options:
    - `:content` (required) - List of content blocks
    - `:structured_content` - Structured data for UI rendering (optional)
    - `:_meta` - Additional metadata (optional)

  ## Examples

      iex> McpServer.Tool.CallResult.new(
      ...>   content: [McpServer.Tool.Content.text("hello")],
      ...>   structured_content: %{"greeting" => "hello"}
      ...> )
      %McpServer.Tool.CallResult{
        content: [%McpServer.Tool.Content.Text{text: "hello"}],
        structured_content: %{"greeting" => "hello"}
      }
  """
  @spec new(keyword()) :: t()
  def new(opts) do
    %__MODULE__{
      content: Keyword.fetch!(opts, :content),
      structured_content: Keyword.get(opts, :structured_content),
      _meta: Keyword.get(opts, :_meta)
    }
  end

  # The value a tool handler is expected to return. The compile-time enforcement comes from the
  # clause patterns of `handle_tool_result/2`, not from this typespec.
  @typep tool_return :: {:ok, t()} | {:ok, list()} | {:error, term()}

  @doc false
  # Runtime "type boundary" for tool handler results, called from the `call_tool/3` clauses
  # generated by `McpServer.Router`.
  #
  # Starting with Elixir 1.20 / OTP 29, the set-theoretic type checker analyses how a handler's
  # return value is consumed. By routing that value through the pattern-matched clauses below
  # (rather than inlining the matching into the macro-generated `call_tool/3`), two things hold
  # at every consumer:
  #
  #   * Valid returns — `{:ok, %CallResult{}}`, `{:ok, list}`, `{:error, reason}` — are a subtype
  #     of the accepted union, so they produce no warning.
  #   * A handler whose inferred return type is NOT in that union (e.g. a bare atom or a
  #     `{:done, _}` tuple) is reported as an incompatible argument at the consumer's
  #     `use McpServer.Router` site; under `warnings_as_errors` this becomes a hard compile error.
  #
  # Enforcement comes from the clause *patterns*, not from `@spec`/`@typep`: there is deliberately
  # NO catch-all clause, since a catch-all would widen the inferred parameter type back to `term()`
  # and silence the check. Runtime-only bad returns (from handlers the checker can only infer as
  # `dynamic()`) therefore raise `FunctionClauseError`, which the macro's surrounding `try/rescue`
  # maps to `{:error, _}`.
  @spec handle_tool_result(tool_return(), String.t()) ::
          {:ok, t()} | {:ok, list()} | {:error, term()}
  def handle_tool_result({:ok, %__MODULE__{} = call_result}, tool_name) do
    validated_content = McpServer.Router.validate_tool_result(call_result.content, tool_name)
    {:ok, %__MODULE__{call_result | content: validated_content}}
  end

  def handle_tool_result({:ok, result}, tool_name) do
    {:ok, McpServer.Router.validate_tool_result(result, tool_name)}
  end

  def handle_tool_result({:error, _} = error, _tool_name) do
    error
  end
end

defimpl Jason.Encoder, for: McpServer.Tool.CallResult do
  def encode(value, opts) do
    map = %{"content" => value.content, "isError" => false}
    map = maybe_put(map, "structuredContent", value.structured_content)
    map = maybe_put(map, "_meta", value._meta)

    Jason.Encode.map(map, opts)
  end

  defp maybe_put(map, _key, nil), do: map
  defp maybe_put(map, key, value), do: Map.put(map, key, value)
end
